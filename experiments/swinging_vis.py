@dataclass
class SwingingVisArgs(ExperimentReplayViewerArgs):
    """CLI args for the hammer-only thesis swing visualizer."""

    experiment_dir: Path
    """Path to one experiment results directory."""

    port: int = 8083
    """Viser server port for the thesis hammer swing browser."""

    show_goal: bool = False
    """Whether to draw the generic current-goal mesh overlay."""

    ghost_opacity: float = 0.42
    """Opacity for the two static commanded swing waypoint ghosts."""

    swing_arc_width: float = 7.0
    """Line width for the directed commanded swing arc."""

    swing_marker_arc_fraction: float = 0.35
    """Fraction along the displayed swing arc where the direction marker is drawn."""


# Return whether one manifest entry targets a hammer Lie execution trial.
def is_hammer_lie_entry(entry: Dict[str, object]) -> bool:
    """Return true for hammer-family execution entries generated by the Lie source."""
    return "hammer" in str(entry.get("object_name", "")) and str(entry.get("mode", "")) == "llm_lie"


# Resolve one manifest replay path relative to the experiment root.
def replay_path(experiment_dir: Path, entry: Dict[str, object]) -> Path:
    """Return the absolute replay artifact path for one manifest entry."""
    raw_path = Path(str(entry["replay_artifact_path"]))
    return raw_path if raw_path.is_absolute() else experiment_dir / raw_path


# Return deterministic two-ghost poses from one commanded hammer swing sequence.
def swing_ghost_poses(goals: Sequence[Sequence[float]]) -> list[list[float]]:
    """Return the upright and mid-swing commanded poses used as ghost meshes."""
    if not goals:
        return []
    if len(goals) == 1:
        return [[float(value) for value in goals[0]]]
    return [
        [float(value) for value in goals[0]],
        [float(value) for value in goals[len(goals) // 2]],
    ]


# Return one chevron marker centered on a path segment.
def swing_direction_chevron_segments(
    start_point: Sequence[float],
    end_point: Sequence[float],
    *,
    length: float = 0.028,
    width: float = 0.014,
    fraction: float = 0.5,
    facing_point: Sequence[float] | None = None,
) -> np.ndarray:
    """Return two line segments forming one direction chevron on the swing arc."""
    start = np.asarray(start_point, dtype=float)
    end = np.asarray(end_point, dtype=float)
    direction = end - start
    direction_norm = float(np.linalg.norm(direction))
    if direction_norm <= 1e-8:
        return np.zeros((0, 2, 3), dtype=float)
    direction = direction / direction_norm
    tip = start + float(np.clip(fraction, 0.0, 1.0)) * (end - start)
    if facing_point is None:
        side = np.cross(direction, np.asarray([0.0, 0.0, 1.0], dtype=float))
    else:
        side = np.asarray(facing_point, dtype=float) - tip
        side = side - direction * float(np.dot(side, direction))
    side_norm = float(np.linalg.norm(side))
    if side_norm <= 1e-8:
        side = np.asarray([1.0, 0.0, 0.0], dtype=float)
    else:
        side = side / side_norm
    base = tip - direction * float(length)
    return np.asarray(
        [
            [tip, base + side * float(width)],
            [tip, base - side * float(width)],
        ],
        dtype=float,
    )


# Return the segment and interpolation fraction at one arc-length fraction of a polyline.
def swing_arc_fraction_segment(points: np.ndarray, arc_fraction: float) -> tuple[int, float]:
    """Return the segment index and local fraction at one polyline arc-length fraction."""
    segment_vectors = points[1:] - points[:-1]
    segment_lengths = np.linalg.norm(segment_vectors, axis=1)
    total_length = float(np.sum(segment_lengths))
    if total_length <= 1e-8:
        return 0, 0.5
    target_length = float(np.clip(arc_fraction, 0.0, 1.0)) * total_length
    cumulative_lengths = np.cumsum(segment_lengths)
    segment_index = int(np.searchsorted(cumulative_lengths, target_length, side="left"))
    segment_index = min(segment_index, len(segment_lengths) - 1)
    segment_start_length = (
        0.0 if segment_index == 0 else float(cumulative_lengths[segment_index - 1])
    )
    local_length = float(segment_lengths[segment_index])
    if local_length <= 1e-8:
        return segment_index, 0.5
    fraction = (target_length - segment_start_length) / local_length
    return segment_index, float(np.clip(fraction, 0.0, 1.0))


# Return a directed arc through the commanded swing path between the two ghost poses.
def swing_arc_segments(
    goals: Sequence[Sequence[float]],
    *,
    object_name: str | None = None,
    max_points: int = 32,
    chevron_facing_point: Sequence[float] | None = None,
    chevron_arc_fraction: float = 0.35,
) -> np.ndarray:
    """Return line-segment points plus one small direction marker for the swing arc."""
    if len(goals) < 2:
        return np.zeros((0, 2, 3), dtype=float)
    if max_points < 3:
        raise ValueError("max_points must be at least 3.")

    end_index = max(1, len(goals) // 2)
    stride = max(1, int(np.ceil((end_index + 1) / float(max_points))))
    sampled = [goals[index] for index in range(0, end_index + 1, stride)]
    if sampled[-1] is not goals[end_index]:
        sampled.append(goals[end_index])
    if object_name is None:
        points = np.asarray([[float(value) for value in pose[:3]] for pose in sampled], dtype=float)
    else:
        semantics = get_object_pose_semantics(object_name)
        points = np.asarray(
            [semantic_point_world(pose, semantics.head_center_local) for pose in sampled],
            dtype=float,
        )
    if len(points) < 2:
        return np.zeros((0, 2, 3), dtype=float)

    path_segments = np.asarray(
        [[points[index], points[index + 1]] for index in range(len(points) - 1)],
        dtype=float,
    )
    marker_segment_index, marker_segment_fraction = swing_arc_fraction_segment(
        points,
        chevron_arc_fraction,
    )
    chevron = swing_direction_chevron_segments(
        points[marker_segment_index],
        points[marker_segment_index + 1],
        fraction=marker_segment_fraction,
        facing_point=chevron_facing_point,
    )
    if len(chevron) == 0:
        return path_segments
    return np.concatenate([path_segments, chevron], axis=0)


# Return the active replay source that owns the commanded reference swing trajectory.
def active_replay_source(artifact: OfflineReplayArtifact, mode: str) -> OfflineReplaySource:
    """Return the active replay source, falling back to artifact order when needed."""
    if mode in artifact.sources:
        return artifact.sources[mode]
    if artifact.mode_order:
        return artifact.sources[artifact.mode_order[0]]
    return next(iter(artifact.sources.values()))


class SwingingVisApp:
    """Execution replay browser with hammer-only swing intent overlays."""

    # Initialize the filtered hammer Lie replay browser.
    def __init__(self, args: SwingingVisArgs) -> None:
        """Create one hammer-only execution replay browser for thesis figures."""
        self._args = args
        self._overlay_handles: list[Any] = []
        self._overlay_generation = 0
        manifest = load_replay_manifest(args.experiment_dir, stage="execution")
        self._entries = [
            dict(entry) for entry in manifest["entries"] if is_hammer_lie_entry(dict(entry))
        ]
        if not self._entries:
            raise ValueError(
                "No hammer llm_lie execution replay entries found. "
                "Run the execution benchmark with replay artifacts first."
            )
        self._labels = [replay_entry_label(entry, stage="execution") for entry in self._entries]
        self._entry_by_label = dict(zip(self._labels, self._entries))
        first_artifact, first_viewer_artifacts = self._load_entry(self._entries[0])
        self._artifact = first_artifact
        self._artifacts = first_viewer_artifacts
        initial_object_name = self._artifact.sources[self._artifact.mode_order[0]].object_name
        self.viewer = ToolTrajectoryViewer(
            object_name=initial_object_name,
            artifacts=self._artifacts,
            port=args.port,
            use_tabs=False,
            show_robot=True,
            show_goal=args.show_goal,
            target_grid_xy=load_experiment_target_grid_xy(args.experiment_dir),
            preloaded_object_names=tuple(
                dict.fromkeys(str(entry["object_name"]) for entry in self._entries)
            ),
        )
        self._hide_debug_overlays()
        self._suppress_trial_callback = False
        self.trial_dropdown = self.viewer.server.gui.add_dropdown(
            "Hammer Lie Trial",
            options=tuple(self._labels),
            initial_value=self._labels[0],
        )
        self.viewer.server.gui.add_markdown(
            "**Swinging Visualization**  \n"
            "Orange meshes show the commanded upright and mid-swing Lie goals. "
            "The small orange marker shows the commanded swing direction."
        )
        self.trial_dropdown.on_update(lambda _: self._on_trial_change())
        self.refresh_swing_overlay()

    # Load one filtered manifest entry into replay artifact and viewer artifacts.
    def _load_entry(self, entry: Dict[str, object]):
        """Return parsed replay artifact plus viewer artifacts for one manifest entry."""
        artifact = load_offline_replay_artifact(replay_path(self._args.experiment_dir, entry))
        viewer_artifacts = [
            replay_source_to_goal_source_artifact(artifact, mode=mode)
            for mode in artifact.mode_order
        ]
        return artifact, viewer_artifacts

    # Hide semantic debugging aids so the thesis scene stays focused on the swing.
    def _hide_debug_overlays(self) -> None:
        """Hide semantic axis and implied-contact debug handles in the replay viewer."""
        for handle_name in (
            "head_axis_line",
            "strike_face_axis_line",
            "implied_contact_marker",
            "strike_target_marker",
        ):
            handle = getattr(self.viewer, handle_name, None)
            if handle is not None and hasattr(handle, "visible"):
                handle.visible = False

    # Remove old overlay handles while tolerating handles without explicit removal support.
    def _clear_swing_overlay(self) -> None:
        """Hide or remove all existing swing overlay scene nodes."""
        for handle in self._overlay_handles:
            try:
                if hasattr(handle, "remove"):
                    handle.remove()
                    continue
            except Exception:
                pass
            if hasattr(handle, "visible"):
                handle.visible = False
        self._overlay_handles = []
        self._overlay_generation += 1

    # Add one tracked overlay handle so it can be cleared on trial changes.
    def _track_overlay_handle(self, handle: Any) -> Any:
        """Store one overlay handle and return it for inline setup."""
        if handle is not None:
            self._overlay_handles.append(handle)
        return handle

    # Draw the two commanded hammer ghost meshes.
    def _add_swing_ghosts(self, *, object_name: str, goals: Sequence[Sequence[float]]) -> None:
        """Draw upright and mid-swing commanded goal meshes as orange ghosts."""
        for index, pose in enumerate(swing_ghost_poses(goals)):
            frame_name = f"/swinging_vis/ghosts/g{self._overlay_generation:04d}_{index:02d}"
            frame = self.viewer.server.scene.add_frame(
                frame_name,
                position=np.asarray(pose[:3], dtype=float),
                wxyz=quaternion_xyzw_to_wxyz(pose[3:]),
                show_axes=False,
            )
            self._track_overlay_handle(frame)
            urdf_handle = self.viewer._add_urdf_safe(
                NAME_TO_OBJECT[object_name].urdf_path,
                root_node_name=frame_name,
                mesh_color_override=(255, 128, 0, float(self._args.ghost_opacity)),
            )
            self._track_overlay_handle(urdf_handle)

    # Draw one directed swing arc through the commanded object-origin path.
    def _add_swing_arc(self, goals: Sequence[Sequence[float]], *, object_name: str) -> None:
        """Draw one directed orange arc over the commanded swing path."""
        segments = swing_arc_segments(
            goals,
            object_name=object_name,
            chevron_facing_point=(0.0, 0.8, 0.0),
            chevron_arc_fraction=self._args.swing_marker_arc_fraction,
        )
        if len(segments) == 0:
            return
        colors = np.tile(np.asarray([255, 128, 0], dtype=np.uint8), (segments.shape[0], 2, 1))
        line_handle = self.viewer.server.scene.add_line_segments(
            f"/swinging_vis/arc/g{self._overlay_generation:04d}",
            points=segments,
            colors=colors,
            line_width=float(self._args.swing_arc_width),
        )
        self._track_overlay_handle(line_handle)

    # Refresh the static swing overlay from the currently selected execution replay source.
    def refresh_swing_overlay(self) -> None:
        """Rebuild thesis swing overlays from the active source's commanded goals."""
        self._clear_swing_overlay()
        source = active_replay_source(self._artifact, self.viewer.playback.mode)
        goals = source.reference_tool_poses
        object_name = source.object_name or self._artifact.object_name
        if not goals or object_name not in NAME_TO_OBJECT:
            return
        self._add_swing_ghosts(object_name=object_name, goals=goals)
        self._add_swing_arc(goals, object_name=object_name)

    # Handle trial dropdown changes by swapping the active replay and swing overlay.
    def _on_trial_change(self) -> None:
        """Switch the active hammer Lie replay artifact from the selected manifest entry."""
        if self._suppress_trial_callback:
            return
        selected_label = str(self.trial_dropdown.value)
        entry = self._entry_by_label[selected_label]
        self._artifact, self._artifacts = self._load_entry(entry)
        object_name = self._artifact.sources[self._artifact.mode_order[0]].object_name
        self.viewer.switch_object(object_name, self._artifacts)
        self._hide_debug_overlays()
        self.viewer.playback.restart()
        self.viewer.playback.set_frame_index(0)
        self.viewer.playback.set_paused(True, now_sec=None)
        self.viewer._suppress_callbacks = True
        self.viewer.frame_slider.value = 0
        self.viewer._suppress_callbacks = False
        self.viewer.pause_button.name = "Play"
        self.refresh_swing_overlay()

    # Advance cached playback one frame and keep debug overlays hidden.
    def tick(self, now_sec: float) -> int:
        """Advance the active replay and return the current frame index."""
        frame_index = self.viewer.tick(now_sec)
        self._hide_debug_overlays()
        return frame_index

    # Run the replay browser indefinitely.
    def run_forever(self) -> None:
        """Serve the hammer swing browser until interrupted."""
        log_info(f"Swinging visualizer running at {getattr(self.viewer.server, 'url', '')}")
        while True:  # pragma: no cover - interactive runtime
            self.tick(time.monotonic())
            time.sleep(1.0 / 60.0)


# Parse CLI args and run the thesis-focused hammer swing browser.
def main() -> None:
    """Entry point for hammer-only execution replay with swing intent overlays."""
    app = SwingingVisApp(tyro.cli(SwingingVisArgs))
    if app._args.startup_only:
        log_info("Swinging visualizer startup completed.")
        return
    app.run_forever()


if __name__ == "__main__":
    main()
