"""Presentation viewer for thesis screenshots of the full robot-chat system."""

from __future__ import annotations

import time
from dataclasses import dataclass
from pathlib import Path
from typing import Dict

import tyro

from experiments.replay_browser import (
    ExperimentReplayViewerArgs,
    load_replay_manifest,
    replay_entry_label,
)
from experiments.swinging_vis import replay_path
from geometric_tool_planning.viewer import ToolTrajectoryViewer
from laptop.artifacts import load_offline_replay_artifact, replay_source_to_goal_source_artifact
from laptop.utils import log_info, render_chat_html

SYSTEM_VIS_CHAT_HISTORY: tuple[tuple[str, str], ...] = (
    ("assistant", "Hey, I'm your friendly robot assistant. How can I help you?"),
    ("user", "swing on the left side of the table"),
    (
        "assistant",
        "Swinging with the claw_hammer on the left side of the table.",
    ),
)


@dataclass
class SystemVisArgs(ExperimentReplayViewerArgs):
    """CLI args for the thesis full-system presentation viewer."""

    experiment_dir: Path
    """Path to one experiment results directory."""

    port: int = 8085
    """Viser server port for the thesis system figure browser."""

    frame_index: int = 0
    """Execution replay frame used for the static robot/tool scene."""


# Return whether one manifest entry targets a hammer Lie execution trial.
def is_system_hammer_lie_entry(entry: Dict[str, object]) -> bool:
    """Return true for hammer-family execution entries generated by the Lie source."""
    return "hammer" in str(entry.get("object_name", "")) and str(entry.get("mode", "")) == "llm_lie"


# Return viewer handle names hidden for a clean thesis system figure.
def hidden_system_overlay_handle_names() -> tuple[str, ...]:
    """Return semantic and target overlay handle names hidden by the system visualizer."""
    return (
        "head_axis_line",
        "strike_face_axis_line",
        "implied_contact_marker",
        "strike_target_marker",
    )


# Return a clamped frame index for one viewer playback state.
def clamped_system_frame_index(frame_index: int, max_frame_index: int) -> int:
    """Return a frame index clamped to the replay slider range."""
    return max(0, min(int(frame_index), int(max_frame_index)))


class SystemVisApp:
    """Static robot-chat presentation viewer for thesis system figures."""

    # Initialize the thesis system figure viewer.
    def __init__(self, args: SystemVisArgs) -> None:
        """Create a static hammer replay scene with a non-executing chat panel."""
        self._args = args
        manifest = load_replay_manifest(args.experiment_dir, stage="execution")
        self._entries = [
            dict(entry) for entry in manifest["entries"] if is_system_hammer_lie_entry(dict(entry))
        ]
        if not self._entries:
            raise ValueError(
                "No hammer llm_lie execution replay entries found. "
                "Run the execution benchmark with replay artifacts first."
            )
        self._labels = [replay_entry_label(entry, stage="execution") for entry in self._entries]
        self._entry_by_label = dict(zip(self._labels, self._entries))
        self._artifact, self._artifacts = self._load_entry(self._entries[0])
        initial_object_name = self._artifact.sources[self._artifact.mode_order[0]].object_name
        self.viewer = ToolTrajectoryViewer(
            object_name=initial_object_name,
            artifacts=self._artifacts,
            port=args.port,
            use_tabs=True,
            show_robot=True,
            show_goal=False,
            target_grid_xy=(),
            preloaded_object_names=tuple(
                dict.fromkeys(str(entry["object_name"]) for entry in self._entries)
            ),
        )
        self._hide_debug_overlays()
        self._install_chat_panel()
        self._set_static_frame(args.frame_index)

    # Load one filtered manifest entry into replay artifact and viewer artifacts.
    def _load_entry(self, entry: Dict[str, object]):
        """Return parsed replay artifact plus viewer artifacts for one manifest entry."""
        artifact = load_offline_replay_artifact(replay_path(self._args.experiment_dir, entry))
        viewer_artifacts = [
            replay_source_to_goal_source_artifact(artifact, mode=mode)
            for mode in artifact.mode_order
        ]
        return artifact, viewer_artifacts

    # Hide semantic debug aids and table markers for a clean system figure.
    def _hide_debug_overlays(self) -> None:
        """Hide semantic axis and target debug handles in the replay viewer."""
        for handle_name in hidden_system_overlay_handle_names():
            handle = getattr(self.viewer, handle_name, None)
            if handle is not None and hasattr(handle, "visible"):
                handle.visible = False
        if self.viewer.target_grid_checkbox is not None:
            self.viewer.target_grid_checkbox.value = False
        for marker in getattr(self.viewer, "_target_grid_markers", []):
            if hasattr(marker, "visible"):
                marker.visible = False

    # Add the non-executing hardcoded chat interface used by the thesis screenshot.
    def _install_chat_panel(self) -> None:
        """Populate the chat tab with a static example command and replay controls."""
        chat_container = self.viewer.chat_gui_container or self.viewer.server.gui
        with chat_container:
            self.viewer.server.gui.add_html(render_chat_html(list(SYSTEM_VIS_CHAT_HISTORY)))
            self.viewer.server.gui.add_text("Message (Enter to send)", initial_value="")
            self.viewer.server.gui.add_markdown("---")
            self._chat_run_button = self.viewer.server.gui.add_button("Run Episode")
            self._chat_pause_button = self.viewer.server.gui.add_button("Pause")
            self._chat_stop_button = self.viewer.server.gui.add_button("Stop Episode")
            self._chat_reset_button = self.viewer.server.gui.add_button("Reset")
            self._chat_run_button.on_click(lambda _: self._run_replay_from_chat())
            self._chat_pause_button.on_click(lambda _: self._toggle_replay_pause_from_chat())
            self._chat_stop_button.on_click(lambda _: self._stop_replay_from_chat())
            self._chat_reset_button.on_click(lambda _: self._reset_replay_from_chat())

    # Restart the saved replay from the chat-tab run button.
    def _run_replay_from_chat(self) -> None:
        """Restart the replay artifact and resume playback from the first frame."""
        self.viewer._on_restart()
        self._sync_chat_pause_button()

    # Toggle saved replay playback from the chat-tab pause button.
    def _toggle_replay_pause_from_chat(self) -> None:
        """Toggle replay playback and mirror the pause label into the chat tab."""
        self.viewer._on_pause_toggle()
        self._sync_chat_pause_button()

    # Pause the saved replay from the chat-tab stop button.
    def _stop_replay_from_chat(self) -> None:
        """Pause replay playback without changing the current frame."""
        self.viewer.playback.set_paused(True, now_sec=None)
        self.viewer.pause_button.name = "Play"
        self._sync_chat_pause_button()

    # Reset the saved replay to the deterministic screenshot frame.
    def _reset_replay_from_chat(self) -> None:
        """Return the replay to the configured static frame and keep it paused."""
        self._set_static_frame(self._args.frame_index)
        self._sync_chat_pause_button()

    # Keep the chat-tab pause label aligned with the underlying replay button.
    def _sync_chat_pause_button(self) -> None:
        """Mirror the current replay pause state into the chat pause button label."""
        if hasattr(self, "_chat_pause_button"):
            self._chat_pause_button.name = "Play" if self.viewer.playback.paused else "Pause"

    # Pause the replay at the chosen frame so the figure is deterministic.
    def _set_static_frame(self, frame_index: int) -> None:
        """Set the viewer to a deterministic paused frame for screenshot capture."""
        clamped_frame_index = clamped_system_frame_index(
            frame_index,
            self.viewer.playback.max_frame_index,
        )
        self.viewer.playback.restart()
        self.viewer.playback.set_frame_index(clamped_frame_index)
        self.viewer.playback.set_paused(True, now_sec=None)
        self.viewer._suppress_callbacks = True
        self.viewer.frame_slider.value = clamped_frame_index
        self.viewer._suppress_callbacks = False
        self.viewer.pause_button.name = "Play"
        self._sync_chat_pause_button()
        self.viewer.tick(time.monotonic())
        self._hide_debug_overlays()

    # Advance the viewer once and keep presentation-only overlays hidden.
    def tick(self, now_sec: float) -> int:
        """Refresh the paused viewer and return the current frame index."""
        frame_index = self.viewer.tick(now_sec)
        self._hide_debug_overlays()
        return frame_index

    # Run the presentation viewer indefinitely.
    def run_forever(self) -> None:
        """Serve the system figure browser until interrupted."""
        log_info(f"System visualizer running at {getattr(self.viewer.server, 'url', '')}")
        while True:  # pragma: no cover - interactive runtime
            self.tick(time.monotonic())
            time.sleep(1.0 / 60.0)


# Parse CLI args and run the thesis-focused system presentation viewer.
def main() -> None:
    """Entry point for the full robot-chat system presentation viewer."""
    app = SystemVisApp(tyro.cli(SystemVisArgs))
    if app._args.startup_only:
        log_info("System visualizer startup completed.")
        return
    app.run_forever()


if __name__ == "__main__":
    main()
