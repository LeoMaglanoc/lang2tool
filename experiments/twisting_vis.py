DEFAULT_TWIST_ARROW_FACING_POINT = (0.0, -0.6, 0.55)


@dataclass
class TwistingVisArgs(ExperimentReplayViewerArgs):
    """CLI args for the screwdriver-only thesis twist visualizer."""

    experiment_dir: Path
    """Path to one experiment results directory."""

    port: int = 8084
    """Viser server port for the thesis screwdriver twist browser."""

    show_goal: bool = False
    """Whether to draw the generic current-goal mesh overlay."""

    twist_arrow_radius_m: float = 0.045
    """Radius of the circular twist arrow in meters."""

    twist_arrow_width: float = 7.0
    """Line width for the screwdriver twist arrow."""

    twist_arrow_tip_offset_m: float = 0.035
    """Offset from the semantic tip back along the twist axis for readability."""

    twist_arrow_facing_point: tuple[float, float, float] = DEFAULT_TWIST_ARROW_FACING_POINT
    """World point used to orient the arrowhead toward the spawn camera."""


# Return whether one manifest entry targets a screwdriver Lie execution trial.
def is_screwdriver_lie_entry(entry: Dict[str, object]) -> bool:
    """Return true for screwdriver-family execution entries generated by the Lie source."""
    return (
        "screwdriver" in str(entry.get("object_name", ""))
        and str(entry.get("mode", "")) == "llm_lie"
    )


# Return the semantic screwdriver twist axis in world coordinates for one pose.
def twist_axis_world(pose: Sequence[float], object_name: str) -> np.ndarray:
    """Return the screwdriver primary twist axis transformed into world coordinates."""
    semantics = get_object_pose_semantics(object_name)
    rotation = quaternion_xyzw_to_rotation_matrix(pose[3:])
    return normalize3(rotation @ normalize3(semantics.primary_axis_local))


# Return the center of the twist arrow ring near the semantic screwdriver tip.
def twist_ring_center_world(
    pose: Sequence[float],
    object_name: str,
    *,
    tip_axis_offset_m: float,
) -> np.ndarray:
    """Return the lifted twist arrow ring center near the semantic screwdriver tip."""
    semantics = get_object_pose_semantics(object_name)
    tip_position = semantic_point_world(pose, semantics.strike_point_local)
    axis_world = twist_axis_world(pose, object_name)
    return tip_position - axis_world * float(tip_axis_offset_m)


# Return an orthonormal basis spanning the plane perpendicular to one twist axis.
def twist_arrow_plane_basis(axis_world: Sequence[float]) -> tuple[np.ndarray, np.ndarray]:
    """Return two stable unit vectors perpendicular to the given twist axis."""
    axis = normalize3(axis_world)
    reference = np.asarray([0.0, 0.0, 1.0], dtype=float)
    if abs(float(np.dot(axis, reference))) > 0.95:
        reference = np.asarray([0.0, 1.0, 0.0], dtype=float)
    first = normalize3(np.cross(axis, reference))
    second = normalize3(np.cross(axis, first))
    return first, second


# Return the arrowhead spread direction biased toward one camera-facing point.
def twist_arrowhead_spread_direction(
    *,
    tip: Sequence[float],
    tangent: Sequence[float],
    fallback_radial: Sequence[float],
    facing_point: Sequence[float] | None,
) -> np.ndarray:
    """Return a unit arrowhead spread vector perpendicular to the arrow tangent."""
    tangent_unit = normalize3(tangent)
    if facing_point is not None:
        facing_vector = np.asarray(facing_point, dtype=float) - np.asarray(tip, dtype=float)
        facing_vector = facing_vector - tangent_unit * float(np.dot(facing_vector, tangent_unit))
        if np.linalg.norm(facing_vector) > 1e-8:
            return normalize3(facing_vector)
    fallback = np.asarray(fallback_radial, dtype=float)
    fallback = fallback - tangent_unit * float(np.dot(fallback, tangent_unit))
    return normalize3(fallback)


# Return a circular screwdriver twist arrow with one compact arrowhead.
def twist_arrow_segments(
    pose: Sequence[float],
    object_name: str,
    *,
    radius_m: float = 0.045,
    tip_axis_offset_m: float = 0.035,
    arc_degrees: float = 300.0,
    num_points: int = 40,
    arrowhead_length_m: float = 0.025,
    arrowhead_width_m: float = 0.014,
    arrowhead_facing_point: Sequence[float] | None = DEFAULT_TWIST_ARROW_FACING_POINT,
) -> np.ndarray:
    """Return line segments for one circular twist arrow around the screwdriver tip."""
    if num_points < 4:
        raise ValueError("num_points must be at least 4.")
    if radius_m <= 0.0:
        raise ValueError("radius_m must be positive.")

    axis = twist_axis_world(pose, object_name)
    center = twist_ring_center_world(
        pose,
        object_name,
        tip_axis_offset_m=tip_axis_offset_m,
    )
    basis_u, basis_v = twist_arrow_plane_basis(axis)
    theta_values = np.linspace(0.0, np.deg2rad(float(arc_degrees)), num_points)
    points = np.asarray(
        [
            center + float(radius_m) * (np.cos(theta) * basis_u + np.sin(theta) * basis_v)
            for theta in theta_values
        ],
        dtype=float,
    )
    path_segments = np.asarray(
        [[points[index], points[index + 1]] for index in range(len(points) - 1)],
        dtype=float,
    )
    tangent = normalize3(points[-1] - points[-2])
    radial = normalize3(points[-1] - center)
    spread = twist_arrowhead_spread_direction(
        tip=points[-1],
        tangent=tangent,
        fallback_radial=radial,
        facing_point=arrowhead_facing_point,
    )
    arrow_base = points[-1] - tangent * float(arrowhead_length_m)
    arrowhead = np.asarray(
        [
            [points[-1], arrow_base + spread * float(arrowhead_width_m)],
            [points[-1], arrow_base - spread * float(arrowhead_width_m)],
        ],
        dtype=float,
    )
    return np.concatenate([path_segments, arrowhead], axis=0)


class TwistingVisApp:
    """Execution replay browser with screwdriver-only twist intent overlays."""

    # Initialize the filtered screwdriver Lie replay browser.
    def __init__(self, args: TwistingVisArgs) -> None:
        """Create one screwdriver-only execution replay browser for thesis figures."""
        self._args = args
        self._overlay_handles: list[Any] = []
        self._overlay_generation = 0
        manifest = load_replay_manifest(args.experiment_dir, stage="execution")
        self._entries = [
            dict(entry) for entry in manifest["entries"] if is_screwdriver_lie_entry(dict(entry))
        ]
        if not self._entries:
            raise ValueError(
                "No screwdriver llm_lie execution replay entries found. "
                "Run the execution benchmark with replay artifacts first."
            )
        self._labels = [replay_entry_label(entry, stage="execution") for entry in self._entries]
        self._entry_by_label = dict(zip(self._labels, self._entries))
        first_artifact, first_viewer_artifacts = self._load_entry(self._entries[0])
        self._artifact = first_artifact
        self._artifacts = first_viewer_artifacts
        initial_object_name = self._artifact.sources[self._artifact.mode_order[0]].object_name
        self.viewer = ToolTrajectoryViewer(
            object_name=initial_object_name,
            artifacts=self._artifacts,
            port=args.port,
            use_tabs=False,
            show_robot=True,
            show_goal=args.show_goal,
            target_grid_xy=load_experiment_target_grid_xy(args.experiment_dir),
            preloaded_object_names=tuple(
                dict.fromkeys(str(entry["object_name"]) for entry in self._entries)
            ),
        )
        self._hide_debug_overlays()
        self._suppress_trial_callback = False
        self.trial_dropdown = self.viewer.server.gui.add_dropdown(
            "Screwdriver Lie Trial",
            options=tuple(self._labels),
            initial_value=self._labels[0],
        )
        self.viewer.server.gui.add_markdown(
            "**Twisting Visualization**  \n"
            "The gold circular arrow shows the commanded screwdriver twist direction."
        )
        self.trial_dropdown.on_update(lambda _: self._on_trial_change())
        self.refresh_twist_overlay()

    # Load one filtered manifest entry into replay artifact and viewer artifacts.
    def _load_entry(self, entry: Dict[str, object]):
        """Return parsed replay artifact plus viewer artifacts for one manifest entry."""
        artifact = load_offline_replay_artifact(replay_path(self._args.experiment_dir, entry))
        viewer_artifacts = [
            replay_source_to_goal_source_artifact(artifact, mode=mode)
            for mode in artifact.mode_order
        ]
        return artifact, viewer_artifacts

    # Hide semantic debugging aids so the thesis scene stays focused on the twist.
    def _hide_debug_overlays(self) -> None:
        """Hide semantic axis and target debug handles in the replay viewer."""
        for handle_name in (
            "head_axis_line",
            "strike_face_axis_line",
            "implied_contact_marker",
            "strike_target_marker",
        ):
            handle = getattr(self.viewer, handle_name, None)
            if handle is not None and hasattr(handle, "visible"):
                handle.visible = False

    # Remove old overlay handles while tolerating handles without explicit removal support.
    def _clear_twist_overlay(self) -> None:
        """Hide or remove all existing twist overlay scene nodes."""
        for handle in self._overlay_handles:
            try:
                if hasattr(handle, "remove"):
                    handle.remove()
                    continue
            except Exception:
                pass
            if hasattr(handle, "visible"):
                handle.visible = False
        self._overlay_handles = []
        self._overlay_generation += 1

    # Add one tracked overlay handle so it can be cleared on trial changes.
    def _track_overlay_handle(self, handle: Any) -> Any:
        """Store one overlay handle and return it for inline setup."""
        if handle is not None:
            self._overlay_handles.append(handle)
        return handle

    # Draw the static twist arrow from the first commanded screwdriver Lie pose.
    def _add_twist_arrow(self, goals: Sequence[Sequence[float]], *, object_name: str) -> None:
        """Draw one gold circular twist arrow around the commanded screwdriver tip."""
        if not goals:
            return
        segments = twist_arrow_segments(
            goals[0],
            object_name,
            radius_m=float(self._args.twist_arrow_radius_m),
            tip_axis_offset_m=float(self._args.twist_arrow_tip_offset_m),
            arrowhead_facing_point=self._args.twist_arrow_facing_point,
        )
        if len(segments) == 0:
            return
        colors = np.tile(np.asarray([242, 201, 48], dtype=np.uint8), (segments.shape[0], 2, 1))
        line_handle = self.viewer.server.scene.add_line_segments(
            f"/twisting_vis/arrow/g{self._overlay_generation:04d}",
            points=segments,
            colors=colors,
            line_width=float(self._args.twist_arrow_width),
        )
        self._track_overlay_handle(line_handle)

    # Refresh the static twist overlay from the currently selected execution replay source.
    def refresh_twist_overlay(self) -> None:
        """Rebuild thesis twist overlays from the active source's commanded goals."""
        self._clear_twist_overlay()
        source = active_replay_source(self._artifact, self.viewer.playback.mode)
        object_name = source.object_name or self._artifact.object_name
        if "screwdriver" not in object_name:
            return
        self._add_twist_arrow(source.reference_tool_poses, object_name=object_name)

    # Handle trial dropdown changes by swapping the active replay and twist overlay.
    def _on_trial_change(self) -> None:
        """Switch the active screwdriver Lie replay artifact from the selected manifest entry."""
        if self._suppress_trial_callback:
            return
        selected_label = str(self.trial_dropdown.value)
        entry = self._entry_by_label[selected_label]
        self._artifact, self._artifacts = self._load_entry(entry)
        object_name = self._artifact.sources[self._artifact.mode_order[0]].object_name
        self.viewer.switch_object(object_name, self._artifacts)
        self._hide_debug_overlays()
        self.viewer.playback.restart()
        self.viewer.playback.set_frame_index(0)
        self.viewer.playback.set_paused(True, now_sec=None)
        self.viewer._suppress_callbacks = True
        self.viewer.frame_slider.value = 0
        self.viewer._suppress_callbacks = False
        self.viewer.pause_button.name = "Play"
        self.refresh_twist_overlay()

    # Advance cached playback one frame and keep debug overlays hidden.
    def tick(self, now_sec: float) -> int:
        """Advance the active replay and return the current frame index."""
        frame_index = self.viewer.tick(now_sec)
        self._hide_debug_overlays()
        return frame_index

    # Run the replay browser indefinitely.
    def run_forever(self) -> None:
        """Serve the screwdriver twist browser until interrupted."""
        log_info(f"Twisting visualizer running at {getattr(self.viewer.server, 'url', '')}")
        while True:  # pragma: no cover - interactive runtime
            self.tick(time.monotonic())
            time.sleep(1.0 / 60.0)


# Parse CLI args and run the thesis-focused screwdriver twist browser.
def main() -> None:
    """Entry point for screwdriver-only execution replay with twist intent overlays."""
    app = TwistingVisApp(tyro.cli(TwistingVisArgs))
    if app._args.startup_only:
        log_info("Twisting visualizer startup completed.")
        return
    app.run_forever()


if __name__ == "__main__":
    main()
